namespace MarquitoUtils.Web.React.Class.Config.WebFront
{
    /// <summary>
    /// Mutualize font sizes and font colors
    /// </summary>
    public class Font
    {
        /// <summary>
        /// Main title font size
        /// </summary>
        public int MainTitleSize { get; set; }
        /// <summary>
        /// Main title font color
        /// </summary>
        public string MainTitleColor { get; set; }
        /// <summary>
        /// Title font size
        /// </summary>
        public int TitleSize { get; set; }
        /// <summary>
        /// Title font color
        /// </summary>
        public string TitleColor { get; set; }
        /// <summary>
        /// Container title font size
        /// </summary>
        public int ContainerTitleSize { get; set; }
        /// <summary>
        /// Container title font color
        /// </summary>
        public string ContainerTitleColor { get; set; }
        /// <summary>
        /// Text font size
        /// </summary>
        public int TextSize { get; set; }
        /// <summary>
        /// Text font color
        /// </summary>
        public string TextColor { get; set; }
        /// <summary>
        /// Link font size
        /// </summary>
        public int LinkSize { get; set; }
        /// <summary>
        /// Link font color
        /// </summary>
        public string LinkColor { get; set; }
    }
}
