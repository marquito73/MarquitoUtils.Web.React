namespace MarquitoUtils.Web.React.Class.Config.WebFront
{
    /// <summary>
    /// Mutualize colors for a theme
    /// </summary>
    public class ColorTheme
    {
        /// <summary>
        /// Main color
        /// </summary>
        public string MainColor { get; set; }
        /// <summary>
        /// Complementary color
        /// </summary>
        public string SecondaryColor { get; set; }
        /// <summary>
        /// Container color
        /// </summary>
        public string ContainerColor { get; set; }
        /// <summary>
        /// Button color
        /// </summary>
        public string ButtonColor { get; set; }
        /// <summary>
        /// Input color
        /// </summary>
        public string InputColor { get; set; }
    }
}
